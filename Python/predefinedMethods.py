def fibbo(n):
    if n <= 1:
        return 1
    else:
        return fibbo(n-1)+fibbo(n-2)
num = int(input("Enter number : "))
print("Fibbonacci sequence : ", end="")
for i in range(num):
    print(fibbo(i), end=" ")
